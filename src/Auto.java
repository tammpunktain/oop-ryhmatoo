public class Auto {
    private String mark;
    private String mudel;
    private int aasta;
    private int labiSoit;

    public Auto(String mark, String mudel, int aasta, int labiSoit) {
        this.mark = mark;
        this.mudel = mudel;
        this.aasta = aasta;
        this.labiSoit = labiSoit;
    }

    public




    public String getMark() {
        return mark;
    }

    public String getMudel() {
        return mudel;
    }

    public int getAasta() {
        return aasta;
    }

    public int getLabiSoit() {
        return labiSoit;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }

    public void setMudel(String mudel) {
        this.mudel = mudel;
    }

    public void setAasta(int aasta) {
        this.aasta = aasta;
    }

    public void setLabiSoit(int labiSoit) {
        this.labiSoit = labiSoit;
    }
}
